    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        int panjang = tampil.getText().length();
        int nomer   = tampil.getText().length() -1;
        String store;
        if (panjang > 0)
        {
            StringBuilder kembali = new StringBuilder(tampil.getText());
            kembali.deleteCharAt(nomer);
            store   = kembali.toString();
            tampil.setText(store);
        }
    }//GEN-LAST:event_hapusActionPerformed

