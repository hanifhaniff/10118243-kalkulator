    public void proses_aritmatika() {
        switch(kalkulasi)
        {
            case 1:
                jawab   = nomer + Double.parseDouble(tampil.getText());
                tampil.setText(String.format("%.0f",jawab));
                break;
            case 2:
                jawab   = nomer - Double.parseDouble(tampil.getText());
                tampil.setText(String.format("%.0f",jawab));
                break;
            case 3:
                jawab   = nomer * Double.parseDouble(tampil.getText());
                tampil.setText(String.format("%.0f",jawab));
                break;
            case 4:
                jawab   = nomer / Double.parseDouble(tampil.getText());
                tampil.setText(String.format("%.0f",jawab));
                break;
        }
        
        
        
        
    }
